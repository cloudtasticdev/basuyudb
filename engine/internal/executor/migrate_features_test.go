// TestIdentityAutoIncrement proves GENERATED ... AS IDENTITY columns auto-assign
// from an implicit sequence exactly like SERIAL.
func TestIdentityAutoIncrement(t *testing.T) {
	ex, done := newIdxExec(t)
	defer done()
	sess := testSession(t)

	run(t, ex, sess, "CREATE TABLE t (id INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY, name TEXT)")
	run(t, ex, sess, "INSERT INTO t (name) VALUES ('alice')")
	run(t, ex, sess, "INSERT INTO t (name) VALUES ('bob')")

	r := run(t, ex, sess, "SELECT id, name FROM t ORDER BY id")
	if len(r.Rows) != 2 || r.Rows[0][0].Text != "1" || r.Rows[1][0].Text != "2" {
		t.Fatalf("identity auto-increment: want ids 1,2 got %v", rowsText(r))
	}
}

