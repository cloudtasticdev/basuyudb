// TestDistinctOn proves DISTINCT ON keeps the first row per key (ordered).
func TestDistinctOn(t *testing.T) {
	ex, done := newIdxExec(t)
	defer done()
	sess := testSession(t)

	run(t, ex, sess, "CREATE TABLE t (id INT PRIMARY KEY, grp INT, v INT)")
	run(t, ex, sess, "INSERT INTO t (id, grp, v) VALUES (1, 1, 100)")
	run(t, ex, sess, "INSERT INTO t (id, grp, v) VALUES (2, 1, 200)")
	run(t, ex, sess, "INSERT INTO t (id, grp, v) VALUES (3, 2, 300)")

	r := run(t, ex, sess, "SELECT DISTINCT ON (grp) grp, v FROM t ORDER BY grp, v")
	if len(r.Rows) != 2 {
		t.Fatalf("DISTINCT ON: want 2 groups got %d (%v)", len(r.Rows), rowsText(r))
	}
	// First of grp=1 by v ascending is v=100; grp=2 is v=300.
	if r.Rows[0][0].Text != "1" || r.Rows[0][1].Text != "100" {
		t.Fatalf("DISTINCT ON: grp 1 want first v=100 got %v", r.Rows[0])
	}
	if r.Rows[1][0].Text != "2" || r.Rows[1][1].Text != "300" {
		t.Fatalf("DISTINCT ON: grp 2 want v=300 got %v", r.Rows[1])
	}
}

// TestLateralLeftJoin proves LEFT JOIN LATERAL correlates the subquery with the
// preceding FROM row.
func TestLateralLeftJoin(t *testing.T) {
	ex, done := newIdxExec(t)
	defer done()
	sess := testSession(t)

	run(t, ex, sess, "CREATE TABLE users (id INT PRIMARY KEY, name TEXT)")
	run(t, ex, sess, "CREATE TABLE orders (id INT PRIMARY KEY, uid INT, amount INT)")
	run(t, ex, sess, "INSERT INTO users (id, name) VALUES (1, 'alice')")
	run(t, ex, sess, "INSERT INTO users (id, name) VALUES (2, 'bob')")
	run(t, ex, sess, "INSERT INTO orders (id, uid, amount) VALUES (10, 1, 50)")
	run(t, ex, sess, "INSERT INTO orders (id, uid, amount) VALUES (11, 1, 75)")

	r := run(t, ex, sess,
		"SELECT users.name, o.amount FROM users LEFT JOIN LATERAL (SELECT amount FROM orders WHERE orders.uid = users.id ORDER BY amount DESC LIMIT 1) o ON true ORDER BY users.id")
	if len(r.Rows) != 2 {
		t.Fatalf("LATERAL: want 2 rows got %d (%v)", len(r.Rows), rowsText(r))
	}
	// alice: max amount 75; bob: no orders -> NULL.
	if r.Rows[0][0].Text != "alice" || r.Rows[0][1].Text != "75" {
		t.Fatalf("LATERAL: alice want amount 75 got %v", r.Rows[0])
	}
	if r.Rows[1][0].Text != "bob" || !r.Rows[1][1].Null {
		t.Fatalf("LATERAL: bob want NULL amount got %v", r.Rows[1])
	}
}

